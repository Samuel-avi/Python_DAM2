for num in range(1, 100):
    if num == 1:
        continue
    elif num == 2:
        print(num)
    else:
        es_primo = True
        for i in range(2, int(num**0.5) + 1):
            if num % i == 0:
                es_primo = False
                break
        if es_primo:
            print(num)

