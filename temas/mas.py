cadena = "hola mundo"

print(cadena.isalpha())

# 1. .isalpha() → True si solo hay letras.
# 2. .isdigit() → True si solo hay dígitos (0–9).
# 3. .isdecimal() → True si solo hay números decimales (base 10).
# 4. .isnumeric() → True si solo hay caracteres numéricos (incluye fracciones, romanos, etc.).
# 5. .isalnum() → True si solo hay letras y números (sin espacios ni símbolos).
# 6. .isspace() → True si solo hay espacios, tabulaciones o saltos de línea.
# 7. .isprintable() → True si todos los caracteres son imprimibles (no incluye \n, \t, etc.).